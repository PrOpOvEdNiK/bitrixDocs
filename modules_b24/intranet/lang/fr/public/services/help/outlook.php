<h2>Comment tout cela fonctionne ? Ajoutons un évènement !</h2>
<p>Comment fonctionne la synchronisation bilatérale ? <b>Ajoutez un nouvel évènement dans le calendrier MS Outlook et cet évènement va apparaître automatiquement dans le calendrier sur le Portail !</b> Faites-le maintenant : </p>

<ul>
 <li>dans la grille du calendrier choisissez le jour pour un nouvel évènement;
 <br />
 </li>

 <li>par un double-clic ouvrez la fenêtre d'ajout du nouvel évènement; </li>

 <li>remplissez les champs Thème, Début, Fin et Description; </li>

 <li>cliquez sur Enregistrer et fermer; </li>

 <li>le nouvel évènement <b>sera ajouté dans un calendrier</b> MS Outlook. </li>
 </ul>

